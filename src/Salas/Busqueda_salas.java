    public void Mostrar(){
        String date = ((JTextField)B_fecha.getDateEditor().getUiComponent()).getText();
        DefaultTableModel modelo = new DefaultTableModel();
        
         String SQL ="select id_evento,id_sala,duracion,h_inicio,nombre,encargado from eventos where fecha='"+date+"'";
        
        try {
        Statement st=con.createStatement();
        ResultSet rs= st.executeQuery(SQL);
        modelo.setColumnIdentifiers(new Object []{"id_evento","id_sala","duracion","h_inicio","nombre","encargado"});
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("id_evento"), rs.getString("id_sala"), rs.getString("duracion"),rs.getTime("h_inicio"),rs.getString("nombre"),rs.getString("encargado")});
            }
              Tabla.setModel(modelo);
        }catch (Exception e) {
            System.out.println("error "+ e.getMessage());
        }
    }
    
    public void MostrarSala(){
        String date = ((JTextField)B_fecha.getDateEditor().getUiComponent()).getText();
        DefaultTableModel modelo = new DefaultTableModel();
        
         String SQL ="select id_sala, nombre,estado from salas ";
        
        try {
        Statement st=con.createStatement();
        ResultSet rs= st.executeQuery(SQL);
        modelo.setColumnIdentifiers(new Object []{"id_sala","nombre","estado"});
            while(rs.next()){
                modelo.addRow(new Object[]{ rs.getString("id_sala"), rs.getString("nombre"),rs.getString("estado")});
            }
              Tabla2.setModel(modelo);
        }catch (Exception e) {
            System.out.println("error "+ e.getMessage());
        }
    }
    
