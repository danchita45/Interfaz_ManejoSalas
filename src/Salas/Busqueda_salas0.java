    public void Mostrar(){
        String date = ((JTextField)B_fecha.getDateEditor().getUiComponent()).getText();
        DefaultTableModel modelo = new DefaultTableModel();
        
         String SQL ="select id_evento,id_sala,duracion,h_inicio from eventos where fecha='"+date+"'";
        
        try {
        Statement st=con.createStatement();
        ResultSet rs= st.executeQuery(SQL);
        modelo.setColumnIdentifiers(new Object []{"id_evento","id_sala","duracion","h_inicio"});
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("id_evento"), rs.getString("id_sala"), rs.getString("duracion"),rs.getInt("h_inicio")});
            }
              Tabla.setModel(modelo);
        }catch (Exception e) {
            System.out.println("error "+ e.getMessage());
        }
    }
