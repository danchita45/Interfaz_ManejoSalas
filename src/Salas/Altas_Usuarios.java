public void Agregarusuario(){
    String SQL = "insert into n_usuarios(id_usuario,tipo,nick,contra) values (?,?,?,?)";
    String ps = String.valueOf(pintxt.getPassword());
    try {
        PreparedStatement pst = con.prepareStatement(SQL);
        pst.setString(1, ID_usuario.getText());
        pst.setString(2, N_tipo.getText());
        pst.setString(3,Nuevo_nick.getText());
        pst.setString(4, ps);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "registro exisoto");
        PIN p= new PIN();
        p.setVisible(true);
        this.dispose();
        
        
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error de registro\n verifique los datos \n" + e.getMessage());
        this.setVisible(true);
        
    }
}
    
    
    
    
    
